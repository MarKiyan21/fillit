#include "fillit.h"

char	*create_tmp_str(char *s, char *t, int i)
{
	int	j;

	j = 0;
	t = (char*)malloc(sizeof(char) * 5);
	while (s[i] != '\n' && s[i])
	{
		t[j] = s[i];
		j++;
		i++;
	}
	t[4] = '\0';
	return (t);
}
